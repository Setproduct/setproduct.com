#!/usr/bin/env node
// Inspiration scraper:
// - Fetches a curated set of component category pages from app.setproduct.com
// - Parses the embedded RSC payload (self.__next_f) for AI inspiration items
//   shape: {id, tags[], prompt, title, like_count, category_ids[], imageModule:{src,...}}
// - Builds a pool of ~POOL_SIZE items and generates data/inspiration.ts
//
// This is a MANUAL pre-deploy step (like `npm run thumbs:blog`), NOT wired into build.
//
// Usage:
//   node scripts/scrape-inspiration.mjs
//   APP_ORIGIN default: https://app.setproduct.com
//   POOL_SIZE default: 80
//
// Output:
//   data/inspiration.ts  - InspirationItem[] pool + INSPIRATION_CATEGORIES

import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');

const APP_ORIGIN = (process.env.APP_ORIGIN || 'https://app.setproduct.com').replace(/\/+$/, '');
const POOL_SIZE = Number(process.env.POOL_SIZE || 300);
const PER_CATEGORY_MAX = Number(process.env.PER_CATEGORY_MAX || 30);
const DESCRIPTION_MAX = 160;
const REQUEST_TIMEOUT_MS = Number(process.env.TIMEOUT || 20000);
const USER_AGENT = 'SetproductInspirationScraper/1.0 (+https://setproduct.com)';

// The 10 popular component categories shown in the Categories column (left).
// Order here = display order. Labels are sentence case.
const INSPIRATION_CATEGORIES = [
  { slug: 'button', label: 'Buttons' },
  { slug: 'input', label: 'Inputs' },
  { slug: 'card', label: 'Cards' },
  { slug: 'table', label: 'Tables' },
  { slug: 'dropdown', label: 'Dropdowns' },
  { slug: 'dialog', label: 'Dialogs' },
  { slug: 'tabs', label: 'Tabs' },
  { slug: 'badge', label: 'Badges' },
  { slug: 'tooltip', label: 'Tooltips' },
  { slug: 'toggle', label: 'Toggles' },
];

// --- helpers ---------------------------------------------------------------

async function fetchWithTimeout(url, options = {}, timeoutMs = REQUEST_TIMEOUT_MS) {
  const ctrl = new AbortController();
  const t = setTimeout(() => ctrl.abort(new Error('timeout')), timeoutMs);
  try {
    return await fetch(url, {
      ...options,
      signal: ctrl.signal,
      headers: {
        'user-agent': USER_AGENT,
        'accept-language': 'en-US,en;q=0.9',
        ...(options.headers || {}),
      },
      redirect: 'follow',
    });
  } finally {
    clearTimeout(t);
  }
}

// Decode an RSC string fragment that may be double-escaped (it lives inside a
// JS string inside HTML, so quotes can appear as \" or \\\" etc.).
function decodeRsc(raw) {
  return raw
    .replace(/\\+"/g, '"') // any run of backslashes before a quote -> quote
    .replace(/\\+n/g, ' ') // newlines -> space
    .replace(/\\+t/g, ' ') // tabs -> space
    .replace(/\\+u0026/g, '&')
    .replace(/\\+u003c/g, '<')
    .replace(/\\+u003e/g, '>')
    .replace(/\\{2,}/g, '\\'); // collapse remaining backslash runs
}

function trimDescription(text) {
  const clean = text.replace(/\s+/g, ' ').trim();
  if (clean.length <= DESCRIPTION_MAX) return clean;
  const cut = clean.slice(0, DESCRIPTION_MAX);
  const lastSpace = cut.lastIndexOf(' ');
  return (lastSpace > 80 ? cut.slice(0, lastSpace) : cut).trim() + '…';
}

// Parse all inspiration objects out of a category page HTML.
function parseItems(html, componentType) {
  // RSC objects are single-escaped JSON inside the HTML.
  const re =
    /\{\\"id\\":\\"([0-9a-f-]{36})\\",\\"tags\\":\[[\s\S]*?\],\\"prompt\\":\\"([\s\S]*?)\\",\\"title\\":\\"([\s\S]*?)\\",\\"like_count\\":\d+,\\"category_ids\\":\[[\s\S]*?\],\\"imageModule\\":\{\\"src\\":\\"([\s\S]*?)\\"/g;
  const items = [];
  const seen = new Set();
  let m;
  while ((m = re.exec(html))) {
    const id = m[1];
    if (seen.has(id)) continue;
    seen.add(id);
    const description = trimDescription(decodeRsc(m[2]));
    const title = decodeRsc(m[3]).trim();
    const image = decodeRsc(m[4]).trim();
    if (!title || !image) continue;
    items.push({
      id,
      componentType,
      title,
      description,
      image,
      href: `${APP_ORIGIN}/components/${componentType}/${id}`,
    });
  }
  return items;
}

// Interleave per-category lists so the pool is balanced across categories.
function interleave(lists) {
  const out = [];
  let added = true;
  let i = 0;
  while (added) {
    added = false;
    for (const list of lists) {
      if (i < list.length) {
        out.push(list[i]);
        added = true;
      }
    }
    i++;
  }
  return out;
}

function serializeData(items) {
  const header = `// AUTO-GENERATED by scripts/scrape-inspiration.mjs — do not edit by hand.
// Re-run \`npm run scrape:inspiration\` to refresh the pool from app.setproduct.com.
// Generated: ${new Date().toISOString()}
import type { InspirationItem, InspirationCategory } from "../types/data";

export const INSPIRATION_CATEGORIES: InspirationCategory[] = ${JSON.stringify(
    INSPIRATION_CATEGORIES,
    null,
    2,
  )};

export const INSPIRATION_ITEMS: InspirationItem[] = ${JSON.stringify(items, null, 2)};
`;
  return header;
}

// --- main ------------------------------------------------------------------

async function main() {
  console.log(`Scraping inspiration from ${APP_ORIGIN} (pool target: ${POOL_SIZE})`);
  const perCategory = [];

  for (const cat of INSPIRATION_CATEGORIES) {
    const url = `${APP_ORIGIN}/components/${cat.slug}`;
    try {
      const res = await fetchWithTimeout(url);
      if (!res.ok) {
        console.warn(`  ! ${cat.slug}: HTTP ${res.status} — skipped`);
        perCategory.push([]);
        continue;
      }
      const html = await res.text();
      const items = parseItems(html, cat.slug).slice(0, PER_CATEGORY_MAX);
      console.log(`  ✓ ${cat.slug}: ${items.length} items`);
      perCategory.push(items);
    } catch (err) {
      console.warn(`  ! ${cat.slug}: ${err?.message || err} — skipped`);
      perCategory.push([]);
    }
  }

  let pool = interleave(perCategory);
  // Global dedupe by id (same item can appear under multiple category pages).
  const seen = new Set();
  pool = pool.filter((it) => (seen.has(it.id) ? false : (seen.add(it.id), true)));
  pool = pool.slice(0, POOL_SIZE);

  if (pool.length === 0) {
    console.error('No items scraped — aborting without writing the data file.');
    process.exitCode = 1;
    return;
  }

  const outPath = path.join(ROOT, 'data', 'inspiration.ts');
  await fs.writeFile(outPath, serializeData(pool), 'utf8');
  console.log(`\nWrote ${pool.length} items to data/inspiration.ts`);
}

main().catch((err) => {
  console.error(err);
  process.exitCode = 1;
});
